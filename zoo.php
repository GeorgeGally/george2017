<p>
		Part social experiment and part social commentary on the absurdity of zoos, <b>The Digital Zoo</b> is an exploration of a continuing theme of my work - seeing how technology can effect our emotions.
</p>

<p>

	Gallery visitors were given the responsibility for the welfare of 5 LED Tamagotchi-like animals.
	e Big 5 can be fed, played with, and nurtured into adulthood. Neglect will lead to a decline in health and ultimately their death. Their well-being can be monitored via SMS in the gallery and via the internet at home. <br>
	Users used SMS text messages to interact, feed, look after, clean and play with with the animals.
	The animals also responded to sound and movement around them, the weather as well as new sentiment and the economy.
	Each animal was created to be similar to its real life counterpart in terms of age, weight, feeding times and lifespan. <br>

</p><p>
	And as a play against wildlife webcams around Africa, such as Africam, and more specifically zoo cams,
	online viewers could also view live webcam feeds of the animals and their environment, the gallery space, while also tracking live data on the animal's well-being.


</p>

<p>
Each animation was individually drawn in photoshop and then converted programatically to be displayed on our custom LED matrixes.
Building the matrixes (pre-Ardunio days) was painstaking to say the least, involving teaching myself electronics while trying to recall some high school science, desiging and printing PCB boards, hundreds of hours of painstaking wiring and hacking the basic stamp to transmit serial data fast enough.
</p>

<p>
Internestingly visitors were more obsessed in interacting with the animals than their welfare - while gallery staff developed strong emotional (and protective) ties to the animals.

</p>


<p>
Because even digital animals shouldn't be kept in cages, after six weeks all the animals had died and the exhibition ended.
The lion, evidently also king of the digital jungle, was the last to go.
</p>

Motion detection, artificial intelligence, installation, LED <br>
Concept, art direction, design, animation, programming <br>
Technologies: Processing, PHP, MySql, SMS, LED, Basic Stamp <br>
Medium: LED, Basic Stamp, Webcam, motion sensors.<br>
