		For the 2014 Standard Chartered Bank Arts in the Park, along with TBWA's Digital Arts Network (DAN) Hong Kong, we created the  automated Robo Maker machine, that used robotic mechanisms to enable audiences to create their own original robots by combining various body parts.
		</p><p>The robots were designed as a collaboration with some of Hong Kong's best known street artists to create 1,000 design permutations.
The booth was designed to resemble a robot factory, with two design machines, where handles are turned to select the robot’s head, body and legs.
</p>
